# 4. Arithmetic Operations in Daily Life
# Task 1: Grocery Store Math
bread = 1.5
pb = 2.5
jelly = 2.0
print("How much bread do you need?")
a = float(input())
print("How many peanut butter jars do you need?")
b = float(input())
print("How many jars of jelly do you need?")
c = float(input())
bread = bread * a
pb = pb * b
jelly = jelly * c
print("Your total cost $", bread + pb + jelly)

